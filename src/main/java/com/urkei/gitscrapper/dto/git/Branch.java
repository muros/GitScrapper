package com.urkei.gitscrapper.dto.git;

/**
 * Branc information from github.
 */
public class Branch {

    /** Branch name. */
    private String name;

    /** Commit data for branch, containing commit SHA. */
    private Commit commit;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Commit getCommit() {
        return commit;
    }

    public void setCommit(Commit commit) {
        this.commit = commit;
    }
}
